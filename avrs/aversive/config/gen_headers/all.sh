for i in *.h ; do
    part=${i%.h}
    part=${part#aversive_}
    if [ "$part" = "parts" ]; then
	continue
    fi
    echo "#${EL}if defined (__AVR_${part}__)" >> ../parts.h
    echo "#include <aversive/parts/$i>" >> ../parts.h
    EL=el
done
