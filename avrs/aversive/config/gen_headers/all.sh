HDR_DIR=headers
XML_DIR=xml

mkdir -p ${HDR_DIR}
mkdir -p ${XML_DIR}

echo "-- generating header files --"

rm -rf ${HDR_DIR}
mkdir -p ${HDR_DIR}/aversive/parts

cd ${XML_DIR}
for i in *.xml ; do
    echo $i
    ../gen_regs.py $i > ../${HDR_DIR}/aversive/parts/${i%.xml}.h
done
cd -

cd ${HDR_DIR}/aversive/parts

